def insert_race_data():
    db = SessionLocal()
    try:
        for world_id, races in RACE_DATA.items():
            # Check existing count
            existing = db.query(WorldRaceAppearance).filter(
                WorldRaceAppearance.world_id == world_id
            ).count()
            
            if existing > 0:
                print(f"[SKIP] {world_id} already has {existing} race records")
                continue
            
            for idx, (race, appearance) in enumerate(races):
                record = WorldRaceAppearance(
                    world_id=world_id,
                    race=race,
                    appearance=appearance,
                    status=1,
                    sort_order=idx
                )
                db.add(record)
            
            db.commit()
            print(f"[OK] {world_id}: inserted {len(races)} race/appearance records")
        
        # Verify
        print("\n--- Verification ---")
        from sqlalchemy import func
        rows = db.query(
            WorldRaceAppearance.world_id,
            func.count(WorldRaceAppearance.id)
        ).group_by(WorldRaceAppearance.world_id).all()
        for r in rows:
            print(f"  {r[0]}: {r[1]} records")
        
    except Exception as e:
        db.rollback()
        print(f"[ERROR] {e}")
        import traceback
        traceback.print_exc()
    finally:
        db.close()


if __name__ == "__main__":
    insert_race_data()
